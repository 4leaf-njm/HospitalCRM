package com.sysout.crm.domain;

public class DepartVO {
	private String depart_code;
	private String depart_name;
	private String depart_field;
	private String depart_tel;
	
	public String getDepart_code() {
		return depart_code;
	}
	public void setDepart_code(String depart_code) {
		this.depart_code = depart_code;
	}
	public String getDepart_name() {
		return depart_name;
	}
	public void setDepart_name(String depart_name) {
		this.depart_name = depart_name;
	}
	public String getDepart_field() {
		return depart_field;
	}
	public void setDepart_field(String depart_field) {
		this.depart_field = depart_field;
	}
	public String getDepart_tel() {
		return depart_tel;
	}
	public void setDepart_tel(String depart_tel) {
		this.depart_tel = depart_tel;
	}
}
